function makeDropdown(m_data, makeFilter) {

    const m_filteredArray = m_data.filter(r => r[0] === makeFilter);

    const m_uniqueList = m_getUniqueValues(m_filteredArray, 1);

    const selectModel = document.getElementById("model");

    m_populateDropDown(selectModel, m_uniqueList);

}

function m_afterDocumentLoads() {
    populateMakeLevelDropDown();
    m_applyDropDown();
}

function m_applyDropDown() {
    const selectMakeValue = document.getElementById("make").value;
    makeDropdown(makeData, selectMakeValue);
}

function m_getUniqueValues(m_data, index) {
    const m_uniqueOptions = new Set();
    m_data.forEach(r => m_uniqueOptions.add(r[index]));
    return [...m_uniqueOptions];
}

function populateMakeLevelDropDown() {
    const m_uniqueList = m_getUniqueValues(makeData, 0);
    const m_el = document.getElementById("make");
    m_populateDropDown(m_el, m_uniqueList);
}


function m_populateDropDown(m_el, listAsArray) {
    m_el.innerHTML = "";

    listAsArray.forEach(m_item => {
        const m_option = document.createElement('option');
        m_option.textContent = m_item;
        m_el.appendChild(m_option);
    });
}

document.getElementById("make").addEventListener("change", m_applyDropDown);
document.addEventListener("DOMContentLoaded", m_afterDocumentLoads);
